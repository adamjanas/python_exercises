def add(a, b):
    print(f"Adding {a} + {b}")
    return a + b
def substract(a, b):
    print(f"Substracting {a} - {b}")
    return a - b
def multiply(a, b):
    print(f"Multiplying {a} * {b}")
    return a * b
def divide(a, b):
    print(f"Dividing {a} / {b}")
    return a / b


number1 = add(1, 2)
number2 = divide(2182, 3232)
number3 = multiply(1231, 432)
number4 = substract(1230, 32)

print(f"1^\t{number1}\n2^\t{number2}\n3^\t{number3}\n4^\t{number4}")









